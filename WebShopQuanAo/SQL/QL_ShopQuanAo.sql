--TẠO DATABASE QL_ShopQuanAo--
CREATE DATABASE QL_ShopQuanAo;

--DÙNG DATABASE QL_ShopQuanAo--
USE QL_ShopQuanAo;

--TẠO BẢNG KhachHang--
CREATE TABLE KhachHang (
	MaKH BIGINT IDENTITY(1,1) PRIMARY KEY,
	HoTenKH NVARCHAR(255) NOT NULL,
	MatKhau NVARCHAR(255) NOT NULL,
	GioiTinh NVARCHAR(5) DEFAULT N'Khác' CHECK (GioiTinh IN (N'Nam', N'Nữ', N'Khác')) NOT NULL,
	SDT VARCHAR(11) CHECK (LEN(SDT) BETWEEN 10 AND 11) NOT NULL,  
	Email NVARCHAR(255) UNIQUE NOT NULL,
	DiaChi NVARCHAR(255),                                                                                                       
	NgayTao DATETIME DEFAULT GETDATE(),            
	LanDangNhapGanNhat DATETIME NULL,                        
	TrangThai BIT DEFAULT 1,                               
	SoThich NVARCHAR(MAX) NULL                            
);

--TẠO BẢNG NhanVien--
CREATE TABLE NhanVien (
    MaNV BIGINT IDENTITY(1,1) PRIMARY KEY, 
	HoTenNV NVARCHAR(255) NOT NULL,
	MatKhau NVARCHAR(255) NOT NULL,
	GioiTinh NVARCHAR(5) CHECK (GioiTinh IN (N'Nam', N'Nữ')) NOT NULL,
	SDT VARCHAR(11) CHECK (LEN(SDT) BETWEEN 10 AND 11) NOT NULL,
    Email NVARCHAR(255) UNIQUE NOT NULL,                   
	DiaChi NVARCHAR(255),
	Luong DECIMAL(18,2) CHECK (Luong >= 0) NOT NULL,
    NgayTao DATETIME DEFAULT GETDATE(), 
	LanDangNhapGanNhat DATETIME NULL,
    TrangThai BIT DEFAULT 1,
	LoaiNV NVARCHAR(50) DEFAULT N'Nhân viên' CHECK (LoaiNV IN (N'Admin', N'Nhân viên')) 
);

--TẠO BẢNG DanhMuc--
CREATE TABLE DanhMuc (
    MaDanhMuc INT IDENTITY(1,1) PRIMARY KEY,
    TenDanhMuc NVARCHAR(100) NOT NULL,
    Slug NVARCHAR(150) UNIQUE,
	Banner NVARCHAR(1000) NOT NULL,
    MaDanhMucCha INT NULL,
    CONSTRAINT FK_DanhMucCha FOREIGN KEY (MaDanhMucCha) REFERENCES DanhMuc(MaDanhMuc),
	CONSTRAINT CHK_DanhMucCha CHECK (MaDanhMucCha IS NULL OR MaDanhMucCha <> MaDanhMuc)
);

--TẠO BẢNG SanPham--
CREATE TABLE SanPham (
    MaSP BIGINT IDENTITY(1,1) PRIMARY KEY,
    TenSP NVARCHAR(255) NOT NULL,
	HinhDaiDien NVARCHAR(1000) NOT NULL,
    ChatLieu NVARCHAR(100),
    Form NVARCHAR(100),  
    MoTaChiTiet NVARCHAR(MAX),
    Gia DECIMAL(18,2) NOT NULL,
    HienThi BIT DEFAULT 1,
    NgayTao DATETIME DEFAULT GETDATE(),
    NgayCapNhat DATETIME DEFAULT GETDATE(),
	MaDanhMuc INT,
	CONSTRAINT FK_SanPham_DanhMuc FOREIGN KEY (MaDanhMuc) REFERENCES DanhMuc(MaDanhMuc)
);

--TẠO BẢNG BienTheSanPham--
CREATE TABLE BienTheSanPham (
    MaBienThe BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaSP BIGINT NOT NULL,
    Size NVARCHAR(50) NULL,
    MauSac NVARCHAR(50) NOT NULL,
    Gia DECIMAL(18,2) NOT NULL,
	SoLuongTon INT DEFAULT 0,
    NgayTao DATETIME DEFAULT GETDATE(),
    CONSTRAINT FK_BienTheSanPham_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP)
);

--TẠO BẢNG HinhAnhSanPham--
CREATE TABLE HinhAnhSanPham (
    MaHinh BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaSP BIGINT NOT NULL,
    MaBienThe BIGINT NULL,
    TenHinh NVARCHAR(1000) NOT NULL,
    ThuTuHinh INT DEFAULT 0,
    CONSTRAINT FK_HinhSanPham_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP),
    CONSTRAINT FK_HinhSanPham_BienTheSanPham FOREIGN KEY (MaBienThe) REFERENCES BienTheSanPham(MaBienThe)
);

--TẠO BẢNG DonHang--
CREATE TABLE DonHang (
    MaDH BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaKH BIGINT NULL,
	MaNV BIGINT NULL,
    SoDon NVARCHAR(50) UNIQUE NOT NULL,
    TrangThai NVARCHAR(50) DEFAULT N'Đang xử lý',
    TongTien DECIMAL(18,2) NOT NULL CHECK (TongTien > 0),
    NgayTao DATETIME DEFAULT GETDATE(),
    NgayCapNhat DATETIME DEFAULT GETDATE(),
    DiaChiGiaoHang NVARCHAR(MAX) NOT NULL,
    CONSTRAINT FK_DonHang_KhachHang FOREIGN KEY (MaKH) REFERENCES KhachHang(MaKH),
	CONSTRAINT FK_DonHang_NhanVien FOREIGN KEY (MaNV) REFERENCES NhanVien(MaNV)
);

--TẠO BẢNG ChiTietDonHang--
CREATE TABLE ChiTietDonHang (
    MaCTDonHang BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaDH BIGINT NOT NULL,
    MaSP BIGINT NOT NULL,
    MaBienThe BIGINT NULL,
	SoLuong INT NOT NULL CHECK (SoLuong > 0),
    Gia DECIMAL(18,2) NOT NULL CHECK (Gia > 0),
    ThanhTien AS (SoLuong * Gia) PERSISTED NOT NULL,
    CONSTRAINT FK_ChiTietDonHang_DonHang FOREIGN KEY (MaDH) REFERENCES DonHang(MaDH),
    CONSTRAINT FK_ChiTietDonHang_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP),
    CONSTRAINT FK_ChiTietDonHang_BienTheSanPham FOREIGN KEY (MaBienThe) REFERENCES BienTheSanPham(MaBienThe)
);

--TẠO BẢNG GioHang--
CREATE TABLE GioHang (
    MaGioHang BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaKH BIGINT NULL,
    MaPhien NVARCHAR(255) NULL,
    NgayTao DATETIME DEFAULT GETDATE(),
    NgayCapNhat DATETIME DEFAULT GETDATE(),
    CONSTRAINT FK_GioHang_KhachHang FOREIGN KEY (MaKH) REFERENCES KhachHang(MaKH)
);

--TẠO BẢNG ChiTietGioHang--
CREATE TABLE ChiTietGioHang (
    MaCTGioHang BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaGioHang BIGINT NOT NULL,
    MaSP BIGINT NOT NULL,
    MaBienThe BIGINT NULL,
    SoLuong INT DEFAULT 1,
    NgayThem DATETIME DEFAULT GETDATE(),
    CONSTRAINT FK_ChiTietGioHang_GioHang FOREIGN KEY (MaGioHang) REFERENCES GioHang(MaGioHang),
    CONSTRAINT FK_ChiTietGioHang_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP),
	CONSTRAINT FK_ChiTietGioHang_BienTheSanPham FOREIGN KEY (MaBienThe) REFERENCES BienTheSanPham(MaBienThe)
);

--TẠO BẢNG DanhGia--
CREATE TABLE DanhGia (
    MaDanhGia BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaSP BIGINT NOT NULL,
    MaKH BIGINT NULL,
    Rating TINYINT CHECK (Rating BETWEEN 1 AND 5) NOT NULL,
    TieuDe NVARCHAR(255),
    NoiDung NVARCHAR(MAX),
    NgayTao DATETIME DEFAULT GETDATE(),
    DaDuyet BIT DEFAULT 0,
    CONSTRAINT FK_DanhGia_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP),
    CONSTRAINT FK_DanhGia_KhachHang FOREIGN KEY (MaKH) REFERENCES KhachHang(MaKH) 
);

--TẠO BẢNG SuKien--
CREATE TABLE SuKien (
    MaSuKien BIGINT IDENTITY(1,1) PRIMARY KEY,
    MaKH BIGINT NULL,
	MaPhien NVARCHAR(255) NULL,
    MaSP BIGINT NULL,
    MaBienThe BIGINT NULL,
    LoaiSuKien NVARCHAR(50) CHECK (LoaiSuKien IN (N'Xem', N'Thêm giỏ hàng', N'Mua', N'Yêu thích')),
    GiaTri NVARCHAR(MAX) NULL,
    NgayTao DATETIME DEFAULT GETDATE(),
    CONSTRAINT FK_SuKien_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP),
	CONSTRAINT FK_SuKien_KhachHang FOREIGN KEY (MaKH) REFERENCES KhachHang(MaKH),
	CONSTRAINT FK_SuKien_BienTheSanPham FOREIGN KEY (MaBienThe) REFERENCES BienTheSanPham(MaBienThe)
);

--TẠO BẢNG GoiY--
CREATE TABLE GoiY (
    MaGoiY BIGINT IDENTITY(1,1) PRIMARY KEY,
    TenGoiY NVARCHAR(255) NOT NULL,
    MoTa NVARCHAR(MAX) NULL,
    PhuongPhap NVARCHAR(50) NOT NULL,
    NgayTao DATETIME DEFAULT GETDATE()
);

--TẠO BẢNG ChiTietGoiY--
CREATE TABLE ChiTietGoiY (
    MaGoiY BIGINT NOT NULL,
    MaSP BIGINT NOT NULL,
    Diem FLOAT NOT NULL CHECK (Diem >= 0),
    PRIMARY KEY (MaGoiY, MaSP),
    CONSTRAINT FK_CT_GoiY_GoiY FOREIGN KEY (MaGoiY) REFERENCES GoiY(MaGoiY),
    CONSTRAINT FK_CT_GoiY_SanPham FOREIGN KEY (MaSP) REFERENCES SanPham(MaSP)
);

--TẠO BẢNG ChiTietSuKienGoiY--
CREATE TABLE ChiTietSuKienGoiY (
    MaSuKien BIGINT NOT NULL,
    MaGoiY BIGINT NOT NULL,
    Diem FLOAT CHECK (Diem >= 0) NULL,
    NgayTao DATETIME DEFAULT GETDATE(),
    PRIMARY KEY (MaSuKien, MaGoiY),
    CONSTRAINT FK_CTSuKienGoiY_SuKien FOREIGN KEY (MaSuKien) REFERENCES SuKien(MaSuKien),
    CONSTRAINT FK_CTSuKienGoiY_GoiY FOREIGN KEY (MaGoiY) REFERENCES GoiY(MaGoiY)
);

